export function getReviewsByTrailerId(trailerId: string): Review[] {
  return reviews.filter((r) => r.trailerId === trailerId);
}

export function getAverageRating(trailerId: string): number {
  const trailerReviews = getReviewsByTrailerId(trailerId);
  if (trailerReviews.length === 0) return 0;
  const sum = trailerReviews.reduce((acc, r) => acc + r.rating, 0);
  return Math.round((sum / trailerReviews.length) * 10) / 10;
}
